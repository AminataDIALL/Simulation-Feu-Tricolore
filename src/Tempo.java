

public class Tempo {
	
	private String temps;
	private String action;
	
	public Tempo(String temps, String action) {
		super();
		this.temps = temps;
		this.action = action;
	}

	public String getTemps() {
		return temps;
	}

	public void setTemps(String temps) {
		this.temps = temps;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	

}
